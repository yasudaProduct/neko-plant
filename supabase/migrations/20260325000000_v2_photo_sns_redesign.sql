ALTER TABLE public.posts       ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.post_images ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.post_likes  ENABLE ROW LEVEL SECURITY;

-- -------------------------------------------------------------
-- 9. RLS ポリシー: posts
-- -------------------------------------------------------------
-- 投稿は誰でも閲覧可能
CREATE POLICY "posts_select_all"
    ON public.posts FOR SELECT
    TO anon, authenticated
    USING (true);

-- 認証済みユーザーは自分の投稿を作成可能
CREATE POLICY "posts_insert_own"
    ON public.posts FOR INSERT
    TO authenticated
    WITH CHECK (
        user_id = (SELECT id FROM public.users WHERE auth_id = auth.uid())
    );

-- 認証済みユーザーは自分の投稿を削除可能
CREATE POLICY "posts_delete_own"
    ON public.posts FOR DELETE
    TO authenticated
    USING (
        user_id = (SELECT id FROM public.users WHERE auth_id = auth.uid())
    );

-- -------------------------------------------------------------
-- 10. RLS ポリシー: post_images
-- -------------------------------------------------------------
-- 投稿画像は誰でも閲覧可能
CREATE POLICY "post_images_select_all"
    ON public.post_images FOR SELECT
    TO anon, authenticated
    USING (true);

-- 認証済みユーザーは自分の投稿に紐づく画像を作成可能
CREATE POLICY "post_images_insert_own"
    ON public.post_images FOR INSERT
    TO authenticated
    WITH CHECK (
        post_id IN (
            SELECT id FROM public.posts
            WHERE user_id = (SELECT id FROM public.users WHERE auth_id = auth.uid())
        )
    );

-- 認証済みユーザーは自分の投稿画像を削除可能
CREATE POLICY "post_images_delete_own"
    ON public.post_images FOR DELETE
    TO authenticated
    USING (
        post_id IN (
            SELECT id FROM public.posts
            WHERE user_id = (SELECT id FROM public.users WHERE auth_id = auth.uid())
        )
    );

