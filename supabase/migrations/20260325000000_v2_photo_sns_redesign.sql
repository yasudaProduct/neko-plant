-- -------------------------------------------------------------
-- 4. 新テーブル作成: posts
-- -------------------------------------------------------------
CREATE TABLE "public"."posts" (
    "id"         integer GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "user_id"    integer NOT NULL,
    "plant_id"   integer NOT NULL,
    "pet_id"     integer,
    "comment"    character varying,
    "created_at" timestamp with time zone NOT NULL DEFAULT now()
);

ALTER TABLE "public"."posts" ADD CONSTRAINT "posts_pkey" PRIMARY KEY ("id");

ALTER TABLE "public"."posts"
    ADD CONSTRAINT "posts_user_id_fkey"
    FOREIGN KEY (user_id) REFERENCES public.users(id) ON DELETE CASCADE;

ALTER TABLE "public"."posts"
    ADD CONSTRAINT "posts_plant_id_fkey"
    FOREIGN KEY (plant_id) REFERENCES public.plants(id) ON DELETE CASCADE;

ALTER TABLE "public"."posts"
    ADD CONSTRAINT "posts_pet_id_fkey"
    FOREIGN KEY (pet_id) REFERENCES public.pets(id) ON DELETE SET NULL;

CREATE INDEX "posts_plant_id_idx" ON public.posts (plant_id);
CREATE INDEX "posts_user_id_idx"  ON public.posts (user_id);
CREATE INDEX "posts_created_at_idx" ON public.posts (created_at DESC);

-- -------------------------------------------------------------
-- 5. 新テーブル作成: post_images
-- -------------------------------------------------------------
CREATE TABLE "public"."post_images" (
    "id"         integer GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "post_id"    integer NOT NULL,
    "image_url"  character varying NOT NULL,
    "order"      integer NOT NULL DEFAULT 0,
    "created_at" timestamp with time zone NOT NULL DEFAULT now()
);

ALTER TABLE "public"."post_images" ADD CONSTRAINT "post_images_pkey" PRIMARY KEY ("id");

ALTER TABLE "public"."post_images"
    ADD CONSTRAINT "post_images_post_id_fkey"
    FOREIGN KEY (post_id) REFERENCES public.posts(id) ON DELETE CASCADE;

CREATE INDEX "post_images_post_id_idx" ON public.post_images (post_id);

-- -------------------------------------------------------------
-- 6. 新テーブル作成: post_likes
-- -------------------------------------------------------------
CREATE TABLE "public"."post_likes" (
    "id"         integer GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "post_id"    integer NOT NULL,
    "user_id"    integer NOT NULL,
    "created_at" timestamp with time zone NOT NULL DEFAULT now()
);

ALTER TABLE "public"."post_likes" ADD CONSTRAINT "post_likes_pkey" PRIMARY KEY ("id");

ALTER TABLE "public"."post_likes"
    ADD CONSTRAINT "post_likes_post_id_fkey"
    FOREIGN KEY (post_id) REFERENCES public.posts(id) ON DELETE CASCADE;

ALTER TABLE "public"."post_likes"
    ADD CONSTRAINT "post_likes_user_id_fkey"
    FOREIGN KEY (user_id) REFERENCES public.users(id) ON DELETE CASCADE;

ALTER TABLE "public"."post_likes"
    ADD CONSTRAINT "post_likes_post_id_user_id_key" UNIQUE (post_id, user_id);

