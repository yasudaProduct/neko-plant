GRANT SELECT, INSERT, UPDATE, DELETE ON public.posts       TO anon, authenticated, service_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.post_images TO anon, authenticated, service_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.post_likes  TO anon, authenticated, service_role;

GRANT USAGE, SELECT ON SEQUENCE public.posts_id_seq       TO anon, authenticated, service_role;
GRANT USAGE, SELECT ON SEQUENCE public.post_images_id_seq TO anon, authenticated, service_role;
GRANT USAGE, SELECT ON SEQUENCE public.post_likes_id_seq  TO anon, authenticated, service_role;

