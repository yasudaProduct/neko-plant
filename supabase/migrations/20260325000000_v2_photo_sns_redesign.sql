INSERT INTO storage.buckets (id, name, public)
VALUES ('posts', 'posts', true)
ON CONFLICT (id) DO NOTHING;

-- -------------------------------------------------------------
-- 14. ストレージ: posts バケットのポリシー設定
-- -------------------------------------------------------------
CREATE POLICY "posts_storage_select_all"
    ON storage.objects FOR SELECT
    TO anon, authenticated
    USING (bucket_id = 'posts');

CREATE POLICY "posts_storage_insert_authenticated"
    ON storage.objects FOR INSERT
    TO authenticated
    WITH CHECK (bucket_id = 'posts' AND auth.uid() IS NOT NULL);

CREATE POLICY "posts_storage_update_own"
    ON storage.objects FOR UPDATE
    TO authenticated
    USING (bucket_id = 'posts' AND auth.uid() = owner);

CREATE POLICY "posts_storage_delete_own"
    ON storage.objects FOR DELETE
    TO authenticated
    USING (bucket_id = 'posts' AND auth.uid() = owner);
